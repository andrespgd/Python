# TUPPLE

tup1 = ('chem', 'math', 31, 58.75)

print(tup1[2])

for x in tup1:
    print(x)

print(type(tup1), '\n'*3)


# DICTIONARY

dic1 = {'cars':4, 'trucks':6}

print(dic1['cars'])

for y in dic1: 
    print(y)

print(type(dic1), '\n'*3)


# LIST

list1 = [33, 44, 55, 66]

print(list1[3])

for z in list1[1:-1]: 
    print(z**2)

print(type(list1), '\n'*3)
